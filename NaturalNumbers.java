public class NaturalNumbers {
    public static void main(String[] args){

        // Loop for Natural numbers
        for(int n=1;n<=100 ; n++){
            System.out.println(n);

        }        

        //Loop for first 100 Even numbers
        for (int even = 0; even<=100; even+=2) {
            System.out.println(even);
            
        }


    }
}
